#include <stdio.h>
/**
 * add - sum of two integers
 * @a: first int
 * @b: second int
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - difference between two integers
 * @a: first int
 * @b: second int
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - product of two integers
 * @a: first int
 * @b: second int
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - quotient of two integers
 * @a: first int
 * @b: second int
 * Return: quotient
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - remainder from division of two integers
 * @a: first int
 * @b: second int
 * Return: modulo quotient
 */
int mod(int a, int b)
{
	return (a % b);
}
